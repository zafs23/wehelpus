          Muniba Mazari Baloch is an activist, anchor artist, model, singer and motivational speaker.  She uses a wheelchair due to injuries sustained in a car accident at the age of 21. </br>
          Accident and recovery
On 27 February 2008, Muniba and her husband were travelling while they had car accident, in which she sustained several major injuries, including broken bones in her arm (both radius and ulna), rib-cage, shoulder blade, collarbone and spine. Her lungs and liver were also deeply cut. Moreover, her entire lower body was left paralysed.Post-surgery, she was left bed-ridden for two years. Physiotherapy started, which helped her recover enough to use a wheelchair.


After treatment for her injuries, Muniba moved to Rawalpindi. Her mother moved in with her to take care of her, which eventually resulted in her parents' divorce.</br> </br>
 <strong>Career</strong> </br>
 Muniba Mazari has gained fame in multiple areas, as an artist, activist, anchor, model, singer and motivational speaker. Most of her career, however, has been built on painting and motivational speaking.
</br> </br>
 <strong>Artist</strong> </br>
Muniba started painting on her hospital bed.Her medium is acrylics on canvas. With the slogan, Let Your Walls Wear Colours, she created her own art brand called Muniba's Canvas. She has presented her work in exhibitions, including a six-day exhibition held in Lahore from 19 April 2016 to 24 April 2016. This exhibition was held at Collectors Galleria and displayed 27 acrylic paintings.</br>

Her first International exhibition was held in Dubai - entitled And I Choose To Live - at the Pakistan Association Dubai. The two-day exhibition - hosted by the Embassy of Pakistan, Poetic Strokes and The Collectors Galleria, Lahore - was inaugurated by Moazzam Ahmad Khan, the Pakistani ambassador to the UAE.

She has displayed her art in several other exhibitions, as well as for charity. </br>
